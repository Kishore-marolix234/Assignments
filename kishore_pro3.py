""" 3.check whether a number is even or not """
# taking the input from the user.
num = int(input("Enter the number: "))
#checking if the reminder of num divisible by 2 is 0 or not
if(num%2==0):
    print("The number is even")#print or display the following statement.
else:
    print("The number is odd")#print this else statement if the condition is not satisfied.   